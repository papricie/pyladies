#Funkce
#Kousek kódu, který provádí určitou činnost a dá se znovu použít. Může přijímat vstupy (argumenty) a vracet výsledek pomocí return


#Argument
#Upřesňující hodnota, která se zadává funkci, aby s ní mohla pracovat.


#Procedura
#Funkce, která nic nevrací, jen něco udělá (např. vypíše text přes print).


#Návratová hodnota
#Výsledek, který funkce vrátí zpět pomocí příkazu return.


#Výraz
#Kousek kódu, který se vyhodnotí na nějakou hodnotu (např. 2 + 3, "Ahoj" * 2)


#Cyklus
#Opakování části kódu, dokud platí určitá podmínka nebo dokud neprojde všechny položky (např. for nebo while).


#Se dvěma argumenty (range(a, b)) vytvoří posloupnost čísel od a do b-1.
#Např. range(2, 5) → 2, 3, 4

#Se třemi argumenty (range(a, b, c)) vytvoří posloupnost od a do b-1 po hodnotch c.
#Např. range(1, 10, 3) → 1, 4, 7

